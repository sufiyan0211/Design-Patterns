public class Human {
    public String name;
    public int age;
    public int weight;
    public String gender;
    public String race;

    public Human(String name, int age, int weight, String gender, String race) {
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.gender = gender;
        this.race = race;
    }
}
